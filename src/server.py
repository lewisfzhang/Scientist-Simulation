@app.context_processor
def override_url_for():
    return dict(url_for=dated_url_for)


def dated_url_for(endpoint, **values):
    if endpoint == 'static':
        filename = values.get('filename', None)
        if filename:
            file_path = os.path.join(app.root_path,
                                 endpoint, filename)
            values['q'] = int(os.stat(file_path).st_mtime)
    return url_for(endpoint, **values)


def init_support():
    # start servers, 8000 is for data accessing, 1234 is main server
    try:
        print('requesting data from http://'+init.server_address+":8000")
        requests.get("http://"+init.server_address+":8000")
        print('request received from http://'+init.server_address+':8000...\n')
    except requests.exceptions.ConnectionError as e:  # server is down, create new server
        print('request failed. creating local server at '+init.server_address+':8000')
        s.Popen('cd ../ && nohup python -m http.server 8000 --bind '+init.server_address+' &', shell=True)
        print('server created!')


# closes other server when program is told to exit
def exit_handler():
    # close local server at 'init.server_address':8000
    print('closing local server at '+init.server_address+':8000')
    s.call('kill -9 $(lsof -ti:8000)', shell=True)
    s.call('cd ../ && rm nohup.out', shell=True)  # removing log file generated by running nohup


