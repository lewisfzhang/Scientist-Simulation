if __name__ == '__main__':
    import init

    # ensure current working directory is in src folder
    if os.getcwd()[-3:] != 'src':
        # assuming we are somewhere inside the git directory
        path = s.Popen('git rev-parse --show-toplevel', shell=True, stdout=s.PIPE).communicate()[0].decode("utf-8")[:-1]
        print('changing working directory from', os.getcwd(), 'to', path)
        os.chdir(path + '/src')

    print(s.Popen('which python', shell=True, stdout=s.PIPE).communicate()[0].decode("utf-8")[:-1])
    print(s.Popen('python --version', shell=True, stdout=s.PIPE).communicate()[0].decode("utf-8")[:-1])

    if len(sys.argv) == 2:  # server address
        try:
            print('checking to see if address at '+sys.argv[1]+' is valid...')
            s.call('nohup python -m http.server 5000 --bind '+sys.argv[1]+' &', shell=True)
            s.call('kill -9 $(lsof -ti:5000)', shell=True)
            init.server_address = sys.argv[1]
        except Exception as e:
            print(e)
            init.server_address = socket.gethostbyname(socket.gethostname())  # assuming user probably wanted ip address
    else:
        init.server_address = socket.gethostbyname(socket.gethostname())

    # run!
    s.call('open http://'+init.server_address+':1234/', shell=True)
    init_support()
    atexit.register(exit_handler)
    app.run(init.server_address, '1234')
